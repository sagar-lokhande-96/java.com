public class output {
    public static void main(String[] args) {

        // Print is used to print the message without a new line
        System.out.print("Hello, World!");  

        // Println is used to print a new line after the message
        System.out.println("Hello World!!");

        // \n is  used to print a new line in the message
        System.out.println("Hello \nworld!!!");
    }
}
